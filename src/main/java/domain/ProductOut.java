package domain;

public class ProductOut {
    private String productOutOfShop;
    private int quantityOutOfShop;

    public String getProductOutOfShop() {
        return productOutOfShop;
    }

    public void setProductOutOfShop(String productOutOfShop) {
        this.productOutOfShop = productOutOfShop;
    }

    public int getQuantityOutOfShop() {
        return quantityOutOfShop;
    }

    public void setQuantityOutOfShop(int quantityOutOfShop) {
        this.quantityOutOfShop = quantityOutOfShop;
    }
}
